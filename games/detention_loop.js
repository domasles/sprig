function loadPixelMap(mapName, pixelArray) {
  if (isTransitioning) return

  isTransitioning = true
  globalMapName = mapName

  let numRows = pixelArray.length
  let numCols = 0

  for (let i = 0; i < numRows; i++) {
    if (numCols < pixelArray[i].length)
      numCols = pixelArray[i].length
  }

  if (numCols > 160 || numRows > 128) {
    isTransitioning = false

    throw new Error("Map dimensions exceed the maximum allowed size of 160x128 pixels.")
    return
  }

  let tileRows = Math.ceil(numRows / 16)
  let tileCols = Math.ceil(numCols / 16)

  let dynamicLegend = [...GLOBAL_LEGEND]

  let uniqueBitmaps = {}
  let symbolIndex = 0

  let tileContainers = new Array(tileCols).fill(null).map(() => [])
  let mapRows = []

  for (let y = 0; y < numRows; y++) {
    let rowData = pixelArray[y]

    for (let col = 0; col < tileCols; col++) {
      let startX = col * 16
      let chunkSegment = rowData.substring(startX, startX + 16)

      if (chunkSegment.length < 16)
        chunkSegment = chunkSegment.padEnd(16, "0")

      tileContainers[col].push(chunkSegment)
    }

    if ((y + 1) % 16 === 0 || y === numRows - 1) {
      let mapRowText = ""

      for (let col = 0; col < tileCols; col++) {
        let bitmapString = tileContainers[col].join("\n")

        if (!uniqueBitmaps[bitmapString]) {
          let assignedChar = DYNAMIC_LEGEND_AVAILABLE_SYMBOLS[symbolIndex]

          if (!assignedChar) {
            isTransitioning = false

            throw new Error("Ran out of unique symbols for the map legend. Please reduce the number of unique tiles.")
            return
          }

          uniqueBitmaps[bitmapString] = assignedChar
          symbolIndex++

          dynamicLegend.push([assignedChar, bitmap`${bitmapString}`])
        }

        mapRowText += uniqueBitmaps[bitmapString]
      }

      mapRows.push(mapRowText)
      tileContainers = tileContainers.map(() => [])
    }
  }

  if (shouldTransition) {
    transitionMap(map`${mapRows.join("\n")}`, dynamicLegend, TRANSITION_ROW_DELAY, TRANSITION_COLUMN_DELAY)
  }

  else {
    setLegend(...dynamicLegend)
    setMap(map`${mapRows.join("\n")}`)
    afterMapLoad()

    shouldTransition = true
    isTransitioning = false
  }
}

function transitionMap(map, legend, rowDelay, columnDelay) {
  const WIDTH = width()
  const HEIGHT = height()

  if (HEIGHT <= 1) {
    setLegend(...legend)
    setMap(map)
    afterMapLoad()

    isTransitioning = false

    return
  }

  let row = 0

  function revealRows() {
    if (row >= HEIGHT) {
      const totalSweepTime = WIDTH * columnDelay

      setTimeout(() => {
        setLegend(...legend)
        setMap(map)
        afterMapLoad()

        isTransitioning = false
      }, totalSweepTime)

      return
    }

    for (let col = 0; col < WIDTH; col++) {
      let currentRow = row
      let currentCol = col

      setTimeout(() => {
        const TILE = getTile(currentCol, currentRow)

        if (TILE && TILE[0])
          TILE[0].type = "~"
      }, col * columnDelay)
    }

    row++
    setTimeout(revealRows, rowDelay)
  }

  revealRows()
}

function afterMapLoad() {
  if (globalMapName == "hall")
    spawnAnomalies(MAX_ANOMALY_SPAWN_CHANCE)

  else if (globalMapName == "number") {
    setTimeout(() => {
      if (globalMapName == "number") {
        if (score >= MAX_SCORE)
          loadPixelMap("congrats", CONGRATS)

        else
          loadPixelMap("hall", HALL)
      }
    }, SCORE_SHOW_TIME)
  }

  else if (globalMapName == "congrats") {
    score = 0

    setTimeout(() => {
      if (globalMapName == "congrats")
        loadPixelMap("title", TITLE)
    }, CONGRATS_SHOW_TIME)
  }
}

function spawnAnomalies(maxChance) {
  for (let i = 0; i < ANOMALIES.length; i++) {
    let anomalyGroup = ANOMALIES[i]

    if (Math.random() < maxChance) {
      anomaliesSpawned = true

      for (let j = 0; j < anomalyGroup.length; j++) {
        let spriteData = anomalyGroup[j]

        let x = spriteData[0]
        let y = spriteData[1]
        let spriteKey = spriteData[2]

        addSprite(x, y, spriteKey)
      }
    }
  }
}

